/** 
 * Entendendo os Operadores Aritimeticos
 */ 

const num1 = 10;
const num2 = 2;

/* + Adição Concatenação */
console.log(num1 + num2);

/* - Subtração */
console.log(num1 - num2);

/* * Multiplicação */
console.log(num1 * num2);

/* / Divisão */
console.log(num1 / num2);

/* ** Potenciação */
console.log(num1 ** num2);

/* % Resto da divisão */
console.log(num1 % num2);